"""
⭐ ================================================
    Turtle图形绘制进阶 - 五角星绘制
================================================

📝 功能说明：
    使用turtle库绘制一个填充的五角星
    展示了循环结构在图形绘制中的应用

🔧 主要知识点：
    • 双色设置 (边框色和填充色)
    • 循环结构 (for循环)
    • 前进和转向 (forward, right)
    • 五角星的数学角度 (144度)
    • 图形填充技术

🎯 学习目标：
    掌握循环在图形绘制中的应用
    理解五角星的绘制原理
    学会使用双色设置
    掌握角度计算技巧

💡 扩展思考：
    可以尝试绘制不同大小的五角星
    可以修改角度绘制其他星形
    可以添加多个五角星组成图案
"""

import turtle                        # 第1行，导入turtle模块
turtle.color("red", "yellow")        # 第2行，设置边框颜色为红，填充色为黄
turtle.begin_fill()                  # 第3行，开始填充
for i in range(5):                   # 第4行，重复5次（重复做下面两语句）
    turtle.forward(200)              # 第5行，前移200像素（即画线200像素）
    turtle.right(144)                # 第6行，右转144度
turtle.end_fill()                    # 第7行，结束填充
turtle.done()                        # 第8行，保留绘图结果
