#贪心算法-零钱兑换
'''
题目: 假设有面额为25分、10分、5分、1分的硬币。
现在要找给顾客 n 分钱，要求使用的硬币数量最少。
请编写一个函数，计算最少需要多少枚硬币。
'''
def min_coins(n):
    """
    计算找零n分所需的最少硬币数（可用面额：25分、10分、5分、1分）
    :param n: 需要找零的总金额（单位：分）
    :return: 最少硬币数
    """
    # 贪心算法思想：每次优先使用面额最大的硬币，这样能保证在当前情况下用最少的硬币数。
    # 因为较大面额的硬币能覆盖更多金额，先用掉它们，
    # 剩下的金额再用次大面额的硬币依次处理，直到金额为0。
    coins = [25, 10, 5, 1]
    count = 0
    for coin in coins:
        if n >= coin:
            num = n // coin  # 当前面额最多能用多少枚
            count += num     # 累加硬币总数
            n -= num * coin  # 剩余金额
    return count

# 测试代码
if __name__ == "__main__":
    n = int(input("请输入需要找零的金额（单位：分）："))
    print(f"最少需要{min_coins(n)}枚硬币")
