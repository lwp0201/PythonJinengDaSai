"""
🌀 ================================================
    Turtle图形绘制进阶 - 螺旋正方形绘制
================================================

📝 功能说明：
    绘制一个逐渐变大的螺旋正方形图案
    展示了变量在循环中的应用和数学计算

🔧 主要知识点：
    • 画笔粗细设置 (pensize)
    • 循环变量应用 (x变量)
    • 数学计算 (10*x)
    • 螺旋图案绘制原理
    • 正方形绘制 (90度转向)

🎯 学习目标：
    理解循环变量的作用
    掌握数学计算在图形绘制中的应用
    学会绘制螺旋图案
    理解图案的数学规律

💡 扩展思考：
    可以尝试不同的增长倍数
    可以修改转向角度绘制其他螺旋
    可以添加颜色变化效果
"""

import turtle
turtle.pensize(5)       	# 第2行，设置线宽5像素
for x in range(20): 		# 第3行，重复20次
      turtle.forward(10*x) 	# 第4行，前移x的10倍像素
      turtle.left(90) 		# 第5行，左转90度
turtle.done()
