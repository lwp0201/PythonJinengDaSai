recite =input("请背诵李白的《静夜思》,每句以,隔开：")    #输入诗歌内容
poem=recite.split(',')                                 #把字符串以","隔开，形成列表
print("输入的诗歌内容为:",poem)                                            #输出输入的诗歌
print("第一句是否正确：",poem[0]=='床前明月光')         #比较第一句是否正确
print("第二句是否正确：",poem[1]=='疑是地上霜')
print("第三句是否正确：",poem[2]=='举头望明月')
print("第四句是否正确：",poem[3]=='低头思故乡')
print()