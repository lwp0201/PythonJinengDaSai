'''
学生体育运动安全事项至关重要，近期，学生因为运动而导致受伤事例屡屡发生
为此，学校在操场入口处设立了运动安全提示电子警示牌，用于提醒学生注意运动过程中的安全问题。

请利用Python的time模块和random模块，编写代码，实现在警示牌中每隔5秒随机显示一条校园运动安全提示。
'''

import time
import random
# 定义校园运动安全提示列表
safety_warnings = [
    "校园运动时，请穿着合适的运动装备。",
    "在进行激烈运动前，请做好充分的热身准备。",
    "注意运动场地的安全，避免在湿滑或不平整的场地进行运动。",
    "在进行团队运动时，请遵守规则，避免发生碰撞和冲突。",
    "如有身体不适或伤病，请及时停止运动并寻求医疗帮助。",
]
while True:
    # 随机选择一条校园运动安全提示
    current_warning = random.choice(safety_warnings)
    print(current_warning)
    # 等待5秒
    time.sleep(5)