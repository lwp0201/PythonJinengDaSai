poem='''爱莲说	宋 周敦颐
水陆草木之花，可爱者甚蕃。晋陶渊明独爱桔。自李唐来，世人甚爱牡丹。予独爱莲之出淤泥而不染，濯清涟而不妖，中通外直，不蔓不枝，香远益清，亭亭净植，可远观而不可亵玩焉。
予谓桔，花之隐逸者也；牡丹，花之富贵者也；莲，花之君子者也。噫！桔之爱，陶后鲜有闻。莲之爱，同予者何人？牡丹之爱，宜乎众矣！'''

countWords=len(poem)                                      #获取字符串总字数
countAi=poem.count('爱')		  #统计“爱”的个数
poem=poem.replace('桔','菊')		  #把“桔”替换成“菊”
print("整篇《爱莲说》的正文总共有{}字".format(countWords))
print("全文中总共出现了{}次“爱”字".format(countAi))
print(poem)