"""
🎨 ================================================
    Turtle图形绘制基础 - 简单圆形绘制
================================================

📝 功能说明：
    这是一个使用Python turtle库绘制简单圆形的入门示例
    展示了turtle图形绘制的基本操作流程

🔧 主要知识点：
    • turtle模块的导入和使用
    • 画笔颜色设置 (color)
    • 圆形绘制 (circle)
    • 绘图窗口保持 (done)

🎯 学习目标：
    掌握turtle库的基本绘图命令
    理解图形绘制的坐标系统
    学会控制画笔的基本属性

💡 扩展思考：
    可以尝试修改圆的半径、颜色等参数
    可以添加更多图形元素
"""

import turtle                 # 第1行，导入turtle模块
turtle.color("red")           # 第2行，设置画笔颜色为红色
turtle.circle(50)             # 第3行，绘制半径50像素的圆
turtle.done()                 # 第4行，保留绘图结果
