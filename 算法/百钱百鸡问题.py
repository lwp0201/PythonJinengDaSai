# 题目: "百钱百鸡"是中国古代经典的数学问题。
# 问题是：用100元钱买100只鸡，
# 其中公鸡5元一只，母鸡3元一只，小鸡1元三只。
# 请问公鸡、母鸡、小鸡各能买多少只？请编程找出所有可能的解。

# 百钱百鸡问题求解
for x in range(0, 21):  # 公鸡最多买20只
    for y in range(0, 34):  # 母鸡最多买33只
        z = 100 - x - y  # 小鸡只数
        if z % 3 == 0:  # 小鸡必须能被3整除
            if 5 * x + 3 * y + z // 3 == 100:
                print(f"公鸡: {x}只, 母鸡: {y}只, 小鸡: {z}只")
